class Solution {
public:
    int maxSubStr(string str){
        //Write your code here
        int cnt = 0, ans = 0;
        for(char ch: str){
            cnt = cnt+(ch == '1')-(ch == '0');
            if(cnt == 0) ans++;
        }
        if(cnt) return -1;
        return ans;
    }
};


