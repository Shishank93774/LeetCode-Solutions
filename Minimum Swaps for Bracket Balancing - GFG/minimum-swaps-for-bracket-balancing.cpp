class Solution{   
public:
    int minimumNumberOfSwaps(string str){
        // code here 
        int sum = 0, ans = 0;
        for(char ch: str){
            if(ch == '[') sum++;
            else{ sum--;
                if(sum < 0) ans += (-sum);
            }
        }
        return ans;
    }
};

