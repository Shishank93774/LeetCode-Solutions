class Solution
{

public:
    int xmod11(string str)
    {
        int ans = 0;
        int f = str.size()%2?1:-1;
        for(char ch: str) ans = (ans + (f*(ch - '0') + 11))%11, f = -f;
        return ans;
    }
};


